# ---------------------------------------------------------------------------
# Cluster-pair explanation (interactive: "why didn't these two playbooks merge?")
# ---------------------------------------------------------------------------

# Closed verdict set so the frontend can colour/icon the result deterministically.
CLUSTER_PAIR_VERDICTS = {
    "semantic_split",      # different commands; embedding gap is the cause
    "behavioural_split",   # similar commands; scalar block kept them apart
    "both",                # both semantic and behavioural diffs contribute
    "marginal_split",      # centroid sim is near threshold; many small things keep them apart
    "near_duplicate",      # sim ≥ threshold; should have merged (likely a config issue)
}

CLUSTER_PAIR_RECOMMENDATIONS = {
    "lower_threshold",          # same operation in spirit; relax τ to merge
    "accept_split",             # genuinely different operations; current split is correct
    "investigate_embedding",    # commands look similar but the embedding rates them dissimilar
    "investigate_scalars",      # the scalar block is over-separating same-behaviour clusters
}


class ClusterPairExplanation(BaseModel):
    """LLM-generated narrative explaining why two HDBSCAN session clusters
    were not merged into the same playbook. Inputs to the LLM are a curated
    subset of the structured analysis from `analyze_cluster_pair`."""
    verdict: str
    evidence: str
    recommendation: str
    rationale: str = ""

    @field_validator("verdict")
    @classmethod
    def _verdict_in_set(cls, v: str) -> str:
        v2 = (v or "").strip().lower()
        return v2 if v2 in CLUSTER_PAIR_VERDICTS else "marginal_split"

    @field_validator("recommendation")
    @classmethod
    def _recommendation_in_set(cls, v: str) -> str:
        v2 = (v or "").strip().lower()
        return v2 if v2 in CLUSTER_PAIR_RECOMMENDATIONS else "accept_split"


